const iconMap = {
  light: SunIcon,
  dark: MoonIcon,
  system: MonitorIcon,
};

const labelMap = {
  light: "Light",
  dark: "Dark",
  system: "System",
};

export function ThemeToggle() {
  const { theme, setTheme } = useTheme();
  const [mounted, setMounted] = useState(false);

  useEffect(() => setMounted(true), []);

  if (!mounted) {
    return <div className="h-8 w-8" />;
  }

  const current = (theme as (typeof themes)[number]) ?? "system";
  const nextIndex = (themes.indexOf(current) + 1) % themes.length;
  const next = themes[nextIndex];
  const Icon = iconMap[current];

  return (
    <button
      type="button"
      onClick={() => setTheme(next)}
      className="ml-auto flex h-8 w-8 items-center justify-center rounded-md text-zinc-600 hover:bg-zinc-100 hover:text-zinc-900 dark:text-zinc-400 dark:hover:bg-zinc-800 dark:hover:text-zinc-100"
      title={`Theme: ${labelMap[current]}. Click for ${labelMap[next]}.`}
    >
      <Icon />
    </button>
  );
}
